# With List
alist=[10,20,30]
for i in alist:
    print(i)
    
#With Range
for i in range(1,5):
    print(i)

#Display all Keys in dict
adict={"Chap1":10,"Chap2":20,"Chap3":30}
for item in adict.keys():
    print("Key :",item)
    print("Value: ",adict[item])

